package com.company;

public class ForLoopDemo
{


    public static void main(String[] args) {
        // for loop begins when x=2
        // and runs till x <10
        int x;
        for (x = 2; x < 10; x++) {

            System.out.println("Running value of x:" + x);

        }
        System.out.println("Final value of x:" + x);
    }
}

